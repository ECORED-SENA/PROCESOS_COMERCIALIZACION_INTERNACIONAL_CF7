      h2 1.3 Modalidades                
    p.mb-5 En el proceso de exportación, se presentan las siguientes modalidades, las cuales se deben conocer y seleccionar cuál de ellas aplicar en el proceso de exportación según los objetivos de la empresa (Ministerio de Hacienda y Crédito Público, 2019):      
    .row.mb-5.justify-content-around.align-items-center
