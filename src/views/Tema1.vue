        .row.mb-5
          LineaTiempoD.color-acento-contenido
            p.text-small(numero="1" titulo="Exportación definitiva") Es la modalidad de exportación que regula la salida de mercancías nacionales o nacionalizadas, del territorio aduanero nacional para su uso o consumo definitivo en otro país. 
            
            p.text-small(numero="2" titulo="Exportación temporal para perfeccionamiento pasivo") Es la modalidad de exportación que regula la salida temporal de mercancías nacionales o nacionalizadas, del territorio aduanero nacional, para ser sometidas a transformación, elaboración o reparación en el exterior o en una zona franca, debiendo ser reimportadas dentro del plazo señalado en la declaración de exportación correspondiente, prorrogables por un año más. 
            
            p.text-small(numero="3" titulo="Exportación temporal para reimportación en el mismo estado") Es la modalidad de exportación que regula la salida temporal de mercancías nacionales o nacionalizadas del territorio aduanero nacional, para atender una finalidad específica en el exterior, en un plazo determinado, durante el cual deberán ser reimportadas sin haber experimentado modificación alguna, con excepción del deterioro normal originado en el uso que de ellas se haga. 
            
            p.text-small(numero="4" titulo="Reexportación") Es la modalidad de exportación que regula la salida definitiva del territorio aduanero nacional, de mercancías que estuvieron sometidas a una modalidad de importación temporal o a la modalidad de transformación y/o ensamble.         

            p.text-small(numero="5" titulo="Reembarque") Es la modalidad de exportación que regula la salida efectiva del Territorio Aduanero Nacional de mercancías procedentes del exterior, que se encuentran en lugar de arribo, en almacenamiento en un depósito habilitado, en un depósito franco o en centros de distribución logística internacional, que no han sido sometidas a una modalidad de importación, ni han quedado en abandono.     

            p.text-small(numero="6" titulo="Exportación por tráfico postal y envíos urgentes") Podrán ser objeto de exportación, por esta modalidad, los envíos de correspondencia, los envíos que salen del territorio nacional por la red oficial de correos y los envíos urgentes, siempre que su valor no exceda de cinco mil dólares de los Estados Unidos de América (US$5.000) y requieran ágil entrega a su destinatario.     
            
            p.text-small(numero="7" titulo="Exportación de muestras sin valor comercial") La solicitud de autorización de embarque y la declaración correspondiente, se tramitarán en la forma prevista en este Decreto para la exportación definitiva, con embarque único y datos definitivos.     
            
            p.text-small(numero="8" titulo="Exportaciones temporales realizadas por viajeros") Serán objeto de esta modalidad de exportación las mercancías nacionales o nacionalizadas que lleven consigo los viajeros que salgan del país y que deseen reimportarlas a su regreso en el mismo estado, sin pago de tributos. No estarán comprendidos en esta modalidad y no serán objeto de declaración, los efectos personales que lleven consigo los viajeros que salgan del territorio aduanero nacional.

            p.text-small(numero="9" titulo="Exportación de menajes y, Programas Especiales de Exportación") Serán objeto de esta modalidad de exportación los menajes de los residentes en el país, que salen del territorio aduanero nacional para fijar su residencia en el exterior. 
